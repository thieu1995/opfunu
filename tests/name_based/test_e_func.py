def test_EasomExpanded_GlobalMin_results():
    ndim = 100
    problem = opfunu.name_based.EasomExpanded(ndim=ndim)
    x = problem.x_global
    result = problem.evaluate(x)
    assert type(result) == np.float64
    assert problem.f_global - result <= problem.epsilon

